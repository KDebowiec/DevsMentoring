number = int(input("podaj numer dnia tygodnia od 1 do 7: "))

if number == 1:
    print('poniedziałek')
elif number == 2:
    print('wtorek')
elif number == 3:
    print('środa')
elif number == 4:
    print('czwartek')
elif number == 5:
    print('piątek')
elif number == 6:
    print('sobota')
elif number == 7:
    print('niedziela')
else:
    print("od 1 do 7 bałwanie")